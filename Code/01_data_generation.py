    "records, rti_counter = [], 1\n",
    "\n",
    "for year, volume in year_volumes.items():\n",
    "    for _ in range(volume):\n",
    "        filing_date    = generate_filing_date(year)\n",
    "        dept           = weighted_choice(departments)\n",
    "        category       = random.choice(dept_category_map[dept])\n",
    "        applicant_type = weighted_choice(applicant_types)\n",
    "        state          = weighted_choice(states)\n",
    "\n",
    "        # Online adoption grows year-on-year (25% in 2014 → ~52% in 2024)\n",
    "        online_w  = min(0.28 + (year - 2014) * 0.025, 0.70)\n",
    "        offline_w = (1 - online_w) * 0.55\n",
    "        post_w    = (1 - online_w) * 0.45\n",
    "        mode = random.choices(\n",
    "            ['Online (RTI Online Portal)', 'Offline (In-Person)', 'By Post'],\n",
    "            weights=[online_w, offline_w, post_w], k=1)[0]\n",
    "\n",
    "        pio = random.choice(pios)\n",
    "\n",
    "        # Response time: normal dist, department-adjusted, COVID-inflated for 2020\n",
    "        delay_factor  = dept_delay_factor.get(dept, 1.0)\n",
    "        if year == 2020:\n",
    "            delay_factor *= 1.30\n",
    "        response_days = max(5, int(np.random.normal(21, 8) * delay_factor))\n",
    "        response_date = filing_date + timedelta(days=response_days)\n",
    "\n",
    "        if response_days <= 30:   response_status = 'Responded On-Time'\n",
    "        elif response_days <= 60: response_status = 'Responded – Delayed (31–60 days)'\n",
    "        else:                     response_status = 'Responded – Significantly Delayed (>60 days)'\n",
    "\n",
    "        if year == 2024 and random.random() < 0.04:\n",
    "            response_status, response_date, response_days = 'Pending', None, None\n",
    "\n",
    "        disposal         = weighted_choice(disposal_types)\n",
    "        rejection_reason = weighted_choice(rejection_reasons) if disposal == 'Rejected' else None\n",
    "\n",
    "        # Appeal probability conditioned on disposal outcome\n",
    "        fa_prob = 0.07\n",
    "        if disposal == 'Rejected':           fa_prob = 0.45\n",
    "        elif disposal == 'Partially Provided': fa_prob = 0.22\n",
    "\n",
    "        first_appeal, fa_date, fa_outcome = 'No', None, None\n",
    "        second_appeal, sa_outcome         = 'No', None\n",
    "\n",
    "        if random.random() < fa_prob:\n",
    "            first_appeal = 'Yes'\n",
    "            ref          = response_date if response_date else filing_date + timedelta(days=30)\n",
    "            fa_date      = ref + timedelta(days=random.randint(10, 55))\n",
    "            fa_outcome   = weighted_choice(first_appeal_outcomes)\n",
    "            if fa_outcome == 'Rejected' and random.random() < 0.28:\n",
    "                second_appeal = 'Yes'\n",
    "                sa_outcome    = weighted_choice(second_appeal_outcomes)\n",
    "\n",
    "        records.append({\n",
    "            'RTI_ID':                f'HPCL-RTI-{str(rti_counter).zfill(5)}',\n",
    "            'Filing_Date':           filing_date.strftime('%Y-%m-%d'),\n",
    "            'Year':                  year,\n",
    "            'Month':                 filing_date.month,\n",
    "            'Month_Name':            filing_date.strftime('%B'),\n",
    "            'Quarter':               get_quarter(filing_date.month),\n",
    "            'Department':            dept,\n",
    "            'Category':              category,\n",
    "            'Applicant_Type':        applicant_type,\n",
    "            'State':                 state,\n",
    "            'Mode_of_Filing':        mode,\n",
    "            'PIO_Assigned':          pio,\n",
    "            'Response_Date':         response_date.strftime('%Y-%m-%d') if response_date else None,\n",
    "            'Response_Time_Days':    response_days,\n",
    "            'Response_Status':       response_status,\n",
    "            'Disposal_Type':         disposal,\n",
    "            'Rejection_Reason':      rejection_reason,\n",
    "            'First_Appeal_Filed':    first_appeal,\n",
    "            'First_Appeal_Date':     fa_date.strftime('%Y-%m-%d') if fa_date else None,\n",
    "            'First_Appeal_Outcome':  fa_outcome,\n",
    "            'Second_Appeal_CIC':     second_appeal,\n",
    "            'Second_Appeal_Outcome': sa_outcome,\n",
    "            'Is_Repeat_Filer':       'Yes' if random.random() < 0.18 else 'No',\n",
    "        })\n",
    "        rti_counter += 1\n",
    "\n",
    "df_raw = pd.DataFrame(records)\n",
    "print(f\"Dataset generated: {len(df_raw):,} records × {len(df_raw.columns)} columns\")"
