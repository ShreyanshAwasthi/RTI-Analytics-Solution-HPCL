    "def weighted_choice(d):\n",
    "    \"\"\"Choose a key from dict d using values as weights.\"\"\"\n",
    "    return random.choices(list(d.keys()), weights=list(d.values()), k=1)[0]\n",
    "\n",
    "def generate_filing_date(year):\n",
    "    \"\"\"Return a datetime for the given year, weighted by seasonal monthly pattern.\"\"\"\n",
    "    mw = monthly_weights.copy()\n",
    "    if year == 2024:\n",
    "        mw[9:] = [0, 0, 0]   # data only up to Sep 2024\n",
    "    month    = random.choices(range(1, 13), weights=mw, k=1)[0]\n",
    "    max_day  = [31, 29 if year % 4 == 0 else 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31][month-1]\n",
    "    return datetime(year, month, random.randint(1, max_day))\n",
    "\n",
    "def get_quarter(month):\n",
    "    return f'Q{(month - 1) // 3 + 1}'\n",
    "\n",
    "print(\"Helper functions defined.\")"
